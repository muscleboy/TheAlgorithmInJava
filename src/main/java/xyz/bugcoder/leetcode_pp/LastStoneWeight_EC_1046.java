    public int lastStoneWeight(int[] stones) {
        PriorityQueue<Integer> queue = new PriorityQueue<>((a, b) -> b - a);
        for (int n : stones) {
            queue.add(n);
        }
        while (queue.size() > 1){
            int a = queue.poll();
            int b = queue.poll();
            if (a > b){
                queue.add(a - b);
            }
        }

        return queue.size() == 0 ? 0 : queue.poll();
    }

